    /// 
    /// </summary>
    public object GetErrorByName(object errName)
    {
        string errDesc = string.Empty;
        errDesc = O9Client.memCached.GetValue("ERR_" + errName.ToString().ToUpper().Trim());
        if (!String.IsNullOrEmpty(errDesc))
            return O9Utils.GetErrorByLang(errDesc);
        else return errDesc;
    }
